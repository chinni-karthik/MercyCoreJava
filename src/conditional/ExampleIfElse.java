package conditional;

public class ExampleIfElse {
    public static void main(String[] args) {
        int number = 9;
        System.out.println("Program Starts!");
        if (number > 0) {
            System.out.println("Positive Number!");
        } else {
            System.out.println("Negative Number!");
        }
        System.out.println("Program Ends!");
    }
}
